for i in range (10): # função range irá retornar uma lista, nesse caso de 10 elementos (0 a 9)
    print (i)

print ('----------')

for i in range (5, 15): # contará de 5 até 14 ( sempre um a menos que o ultimo )
    print (i)

print ('----------')

for i in range (1, 20, 2): # contará de 1 a 19 pulando de 2 em 2 
    print (i) 
