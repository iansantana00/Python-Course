"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio.
Porém, falar que é um tipo sem tipo é mais apropriado.

OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Certo: None 
Errado: none

Quando utilizamos?

- Podemos utilizar None quando queremos criar uma variável e inicializá-lá com um tipo sem tipo, 
antes de recebermos um valor final.

OBS: O tipo None no Python sempre é considerado como False.
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))

